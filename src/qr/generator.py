import tempfile

import qrcode


class QRGenerator:
    _img_save_format = "PNG"

    @staticmethod
    def generate_qr_image(text) -> str:
        qr = qrcode.QRCode(
            version=1,
            error_correction=qrcode.constants.ERROR_CORRECT_L,
            box_size=10,
            border=4,
        )
        qr.add_data(text)
        qr.make(fit=True)
        image_binary = qr.make_image(fill_color="white", back_color="transparent")

        with tempfile.NamedTemporaryFile(delete=False) as img_tmp_file:
            image_binary.save(img_tmp_file, QRGenerator._img_save_format)
            return img_tmp_file.name
